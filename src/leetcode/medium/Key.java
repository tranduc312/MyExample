package leetcode.medium;

public class Key {
    public Key(String key, String item) {
        this.key = key;
        this.item = item;
    }

    private String key;
    private String item;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }
}
